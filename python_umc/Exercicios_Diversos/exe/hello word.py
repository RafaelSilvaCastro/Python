n1 = float(input("digite primeiro numero "))
n2 = float(input("digite segundo numero "))
op = input("digite a operação, '+' para soma, '-' para subtração, '/' para divisão, '*' para multiplicação ")


if op=="+":
        n3=n1+n2
        print("resultado = ", n3)
if op=="-":
        n3=n1-n2
        print("resultado = ", n3)
if op=="/":
        n3=n1/n2
        print("resultado = ", n3)
if op=="*":
        n3=n1*n2
        print("resultado = ", n3)

